        self.sa1 = PointNetSetAbstraction_SpectralNorm(npoint=None, radius=None, nsample=None, in_channel=3, mlp=[64, 128, 1024], group_all=True)
        self.fc1 = nn.utils.parametrizations.spectral_norm( nn.Linear(1024, 512) )
        # self.bn1 = nn.BatchNorm1d(512)
        self.drop1 = nn.Dropout(0.4)
        self.fc2 = nn.utils.parametrizations.spectral_norm( nn.Linear(512, 256) )
        # self.bn2 = nn.BatchNorm1d(256)
        self.drop2 = nn.Dropout(0.4)
        self.fc3 = nn.utils.parametrizations.spectral_norm( nn.Linear(256, num_classes) )
        #   prob head
        self.prob_head = nn.Sequential(
                                        nn.utils.parametrizations.spectral_norm( nn.Linear(num_classes, 1) ),
                                        nn.Sigmoid(),
                                       )

        # self.fc1 = nn.Linear(1024, 512)
